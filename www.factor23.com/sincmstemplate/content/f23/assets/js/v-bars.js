vBarsInit = (id) => {
    let vb = document.getElementById(id)

    let totalBars           = parseInt(vb.getAttribute('data-total'))
    let activeBars          = parseInt(vb.getAttribute('data-active'))
    let colorActive         = vb.getAttribute('data-colorActive') || '#4287f5'
    let colorInactive       = vb.getAttribute('data-colorInactive') || '#dbdbdb'
    let customClassActive   = vb.getAttribute('data-customClassActive') || ''
    let customClassInactive = vb.getAttribute('data-customClassInactive') || ''

    for (let i = 0; i < (totalBars - activeBars); i++) {
        vb.innerHTML += (`
            <div class="${customClassInactive} v-bars-item" style="background-color: ${colorInactive};"></div>
        `)
    }
    for (let i = 0; i < activeBars; i++) {
        vb.innerHTML += (`
            <div class="${customClassActive} v-bars-item" style="background-color: ${colorActive};"></div>
        `)
    }
}

document.addEventListener('DOMContentLoaded', () => {
    let bars = document.querySelectorAll('.v-bars')
    for (let i = 0; i < bars.length; i++) {
        const bar = bars[i];
        bar.setAttribute('id', 'myVerticalBarIdAutoGenerated' + i)
        vBarsInit(bar.getAttribute('id'))
    }
})